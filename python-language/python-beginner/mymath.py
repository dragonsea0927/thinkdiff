def sum(x, y):
    return x + y 
    
def subtract(x, y):
    return x - y 
     
    
class MyMathClass:
    def __init__(self, x, y):
        self.x = x 
        self.y = y 
        
    def sum(self):
        return self.x + self.y 
        
    def subtract(self):
        return self.x - self.y